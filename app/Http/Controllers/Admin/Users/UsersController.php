    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $request->validate([
            'activate' => 'required',
        ]);
  
        $user->update($request->all());
  
        return redirect()->route('admin.users.show', $user->id)->with('status','Статус активации успешно изменен!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Request $request, User $user)
    {
        $user->delete();
        
        if(!$user) {
            $request->session()->flash('status', 'Ошибка удаления пользователя!');
            return redirect()->route('admin.users.index');
        }

        $request->session()->flash('status', 'Пользователь успешно удален!');
        return redirect()->route('admin.users.index');
    }
